#!/usr/bin/env node
// One-off generator: reads src/data/nyc-nta-topo.json (produced by mapshaper, see README)
// and writes src/data/neighborhoods.ts, a static id -> NeighborhoodStaticData map with
// empty recommendations/culturalFlag placeholders. Re-run whenever the topology is
// regenerated; hand-edit the resulting neighborhoods.ts afterwards to fill in real content.
import { readFileSync, writeFileSync } from 'node:fs';
import { fileURLToPath } from 'node:url';
import path from 'node:path';

const __dirname = path.dirname(fileURLToPath(import.meta.url));
const topoPath = path.join(__dirname, '../src/data/nyc-nta-topo.json');
const outPath = path.join(__dirname, '../src/data/neighborhoods.ts');

const topology = JSON.parse(readFileSync(topoPath, 'utf-8'));
const objectKey = Object.keys(topology.objects)[0];
const geometries = topology.objects[objectKey].geometries;

const BORO_NAME_MAP = {
  Manhattan: 'Manhattan',
  Brooklyn: 'Brooklyn',
  Queens: 'Queens',
  Bronx: 'Bronx',
  'The Bronx': 'Bronx',
  'Staten Island': 'Staten Island',
};

const entries = geometries.map((geo) => {
  const props = geo.properties ?? {};
  const id = props.NTA2020;
  const name = props.NTAName;
  const borough = BORO_NAME_MAP[props.BoroName] ?? props.BoroName;
  if (!id || !name || !borough) {
    throw new Error(`Missing expected properties on geography: ${JSON.stringify(props)}`);
  }
  return { id, name, borough };
});

entries.sort((a, b) => a.id.localeCompare(b.id));

const body = entries
  .map(
    ({ id, name, borough }) => `  ${JSON.stringify(id)}: {
    id: ${JSON.stringify(id)},
    name: ${JSON.stringify(name)},
    borough: ${JSON.stringify(borough)},
    recommendations: [],
  },`
  )
  .join('\n');

const output = `import type { NeighborhoodStaticData } from '../types/neighborhood';

// Property key on each TopoJSON geography that holds the neighborhood id (NTA2020 code).
export const NTA_ID_PROPERTY = 'NTA2020';

// Generated by scripts/generate-neighborhoods.mjs from src/data/nyc-nta-topo.json.
// Hand-fill culturalFlag/recommendations for individual neighborhoods as needed.
export const neighborhoods: Record<string, NeighborhoodStaticData> = {
${body}
};
`;

writeFileSync(outPath, output);
console.log(`Wrote ${entries.length} neighborhoods to ${outPath}`);
